package com.sonicmax.bloodrogue.renderer.text;

/**
 *  Float arrays for different colours to be passed to TextRenderer.
 *  Colours are approximations of RGB values shown in comments
 */

public class TextColours {
    public final static float[] WHITE = new float[] {1f, 1f, 1f, 1f}; // rgb (255, 255, 255)
    public final static float[] RED = new float[] {0.9f, 0.1f, 0.1f, 1f}; // rgb(235, 19, 22)
    public final static float[] STATUS_RED = new float[] {1f, 0f, 0f, 1f}; // rgb(255, 33, 33)
    public final static float[] YELLOW = new float[] {0.9f, 0.9f, 0.2f, 1f}; // rgb(243, 240, 41)
}
